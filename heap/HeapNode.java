package heap;

public class HeapNode {
    public String name;
    public Float distance;

    public HeapNode(String name, Float distance) {
        this.name = name;
        this.distance = distance;
    }

    public String getName() {
        return this.name;
    }

    public String getDistance() {
        return this.name;
    }
}
